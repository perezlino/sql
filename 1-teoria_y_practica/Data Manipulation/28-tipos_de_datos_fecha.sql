-- ======================================================
-- ================= TIPOS DATOS FECHA ==================
-- ======================================================

-- Recordar que la fecha en una consulta podemos utilizarla de este modo '20230123' o '2023-01-23'

-- ================
-- === DATETIME ===
-- ================

DECLARE @fecha AS DATETIME = '2015-06-24 12:34:56.124'
SELECT @fecha AS fecha

-- |-------------------------|
-- |          fecha          |
-- |-------------------------|
-- | 2015-06-24 12:34:56.123 |   
-- |-------------------------|

DECLARE @fecha AS DATETIME = '2015-06-24'
SELECT @fecha AS fecha

-- |-------------------------|
-- |          fecha          |
-- |-------------------------|
-- | 2015-06-24 00:00:00.000 |   
-- |-------------------------|


-- ============================================================================================
-- ============================================================================================

/* =================
   === DATETIME2 ===
   ================= */

DECLARE @fecha AS DATETIME2(4) = '2015-06-24 12:34:56.124'
SELECT @fecha AS fecha

-- |-------------------------|
-- |          fecha          |
-- |-------------------------|
-- |2015-06-24 12:34:56.1240 |   
-- |-------------------------|

DECLARE @fecha AS DATETIME2(3) = '2015-06-24 12:34:56.124'
SELECT @fecha AS fecha

-- |-------------------------|
-- |          fecha          |
-- |-------------------------|
-- | 2015-06-24 12:34:56.124 |   
-- |-------------------------|

DECLARE @fecha AS DATETIME2(2) = '2015-06-24 12:34:56.124'
SELECT @fecha AS fecha

-- |-------------------------|
-- |          fecha          |
-- |-------------------------|
-- | 2015-06-24 12:34:56.12  |   
-- |-------------------------|